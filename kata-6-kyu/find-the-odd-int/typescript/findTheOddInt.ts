function findOdd(xs: number[]): number {
  return xs.find((v) => xs.filter((e) => e === v).length % 2 !== 0) || 0
}

//+ ====================================================================================================================
//+ ====================================================================================================================

function findOdd2(xs: number[]): number {
  const map = [...xs.reduce((a, c) => a.set(c, (a.get(c) || 0) + 1), new Map())]
  return (map.find(([, v]) => v % 2) || [])[0]
}

export { findOdd, findOdd2 }
